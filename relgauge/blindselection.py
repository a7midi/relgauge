def _get_first(row: dict, names: tuple[str, ...], default=0.0):
    """Return the first present, non-null value among several historical names."""
    for name in names:
        if name in row:
            val = row[name]
            try:
                if pd.isna(val):
                    continue
            except Exception:
                pass
            return val
    return default


def _call_approx_equalizer_measure_compat(
    ds: S.SCDCDiamond,
    thresholds: Iterable[float],
    initial_pool: InitialPool,
    observer_init: ObserverInit,
    min_count: int,
    require_mutual: bool,
) -> list[dict]:
    """Call approxequalizer across patch versions.

    Some earlier copies of ``relgauge.approxequalizer`` did not expose
    ``min_count`` or ``require_mutual`` in ``approx_equalizer_measure``.  This
    adapter passes only arguments accepted by the installed version, so the
    blind-search module remains compatible with both local trees.
    """
    kwargs = dict(
        ds=ds,
        eq_mode="joint",
        thresholds=tuple(float(t) for t in thresholds),
        initial_pool=initial_pool,
        observer_init=observer_init,
        min_count=min_count,
        require_mutual=require_mutual,
    )
    try:
        sig = inspect.signature(AE.approx_equalizer_measure)
        allowed = set(sig.parameters)
        filtered = {k: v for k, v in kwargs.items() if k in allowed}
        # ``ds`` may be positional-only in a future refactor; support both.
        if "ds" in filtered:
            return AE.approx_equalizer_measure(**filtered)
        return AE.approx_equalizer_measure(ds, **{k: v for k, v in filtered.items() if k != "ds"})
    except TypeError as exc:
        # Last-resort compatibility path for the original simple signature.
        if "unexpected keyword" not in str(exc):
            raise
        return AE.approx_equalizer_measure(
            ds,
            eq_mode="joint",
            thresholds=tuple(float(t) for t in thresholds),
            initial_pool=initial_pool,
            observer_init=observer_init,
        )


def _normalize_approx_row(row: dict, width: int) -> dict:
    """Add canonical names expected by blind selection.

    The approximate-equalizer module went through two naming schemes.  Older
    rows used names such as ``approx_info_qcoords`` and
    ``same_component_mass``; newer rows use names such as
    ``approx_quality_score`` and ``approx_inlier_mass_fraction``.  Normalize so
    selection is based on the same scalar either way.
    """
    out = dict(row)
    residual = float(_get_first(out, (
        "approx_residual_qcoords",
        "aeq_approx_residual_qcoords",
        "residual_qcoords",
    ), 0.0))
    inlier = float(_get_first(out, (
        "approx_inlier_mass_fraction",
        "same_component_mass",
        "aeq_approx_same_component_mass",
        "inlier_mass_fraction",
    ), 1.0))
    cross = float(_get_first(out, (
        "approx_cross_mass_fraction",
        "cross_component_mass",
        "aeq_approx_cross_component_mass",
    ), max(0.0, 1.0 - inlier)))
    balance = float(_get_first(out, (
        "approx_component_balance",
        "component_balance",
        "aeq_component_balance",
    ), 1.0 if residual > 0 else 0.0))
    info = float(_get_first(out, (
        "approx_info_qcoords",
        "aeq_approx_info_qcoords",
        "approx_label_mi_norm",
        "approx_raw_panel_mi_norm",
    ), 0.0))

    # Prefer the module's quality score when present.  Otherwise reconstruct a
    # conservative consistency-only score from residual, inlier mass, and
    # component balance.  The optional info term is only used to break ties; it
    # does not create selected candidates without nontrivial residual.
    quality = _get_first(out, ("approx_quality_score", "quality_score", "aeq_quality_score"), None)
    if quality is None:
        quality = (residual / max(1, int(width))) * max(0.0, inlier) * max(0.0, balance)
        quality += 1e-6 * max(0.0, info)
    quality = float(quality)

    out.update(
        approx_residual_qcoords=float(residual),
        approx_inlier_mass_fraction=float(inlier),
        approx_cross_mass_fraction=float(cross),
        approx_component_balance=float(balance),
        approx_info_qcoords=float(info),
        approx_quality_score=float(quality),
    )
    return out



def _exact_equalizer_metrics(
    ds: S.SCDCDiamond,
    initial_pool: InitialPool = "all",
    observer_init: ObserverInit = "zero",
) -> dict:
    """Strict zero-error interface equalizer metrics for one candidate.

    This is the exact shared alphabet ``S`` used as the primary discovery
    criterion in ``fitness_mode='exact'`` and the second stage of
    ``fitness_mode='two_stage'``.  No product-local quotient is computed here;
    this is the relational interface equalizer only.
    """
    row = IE.interface_equalizer_measure(
        ds,
        eq_mode="joint",
        initial_pool=initial_pool,
        observer_init=observer_init,
        compare_product=False,
    )
    residual = float(row.get("eq_residual_qcoords", 0.0))
    cost = float(row.get("eq_cost_qcoords", math.nan))
    shared = float(row.get("eq_shared_classes_mean", row.get("eq_shared_classes", 0.0)))
    edge = float(row.get("eq_edge_density_mean", row.get("eq_edge_density", math.nan)))
    out = {
        "exact_residual_qcoords": residual,
        "exact_cost_qcoords": cost,
        "exact_residual_per_width": float(residual / ds.w) if ds.w else math.nan,
        "exact_shared_classes_mean": shared,
        "exact_edge_density_mean": edge,
        "exact_selected": bool(residual > 1e-12),
    }
    # Keep the raw exact fields with a prefix so audit scripts can recover the
    # equalizer details without recomputing them.
    for k, v in row.items():
        if k.startswith("eq_"):
            out[f"exact_{k}"] = v
    return out


def _score_candidate(
    out: dict,
    fitness_stage: FitnessStage = "approx",
    selection_threshold_qcoords: float = 1e-9,
    quality_threshold: float = 0.10,
    exact_tie_breaker: float = 1e-6,
) -> dict:
    """Attach blind fitness / selection fields for a chosen stage."""
    approx_quality = float(out.get("approx_quality_score", 0.0))
    approx_residual = float(out.get("approx_residual_qcoords", 0.0))
    exact_residual = float(out.get("exact_residual_qcoords", 0.0))
    exact_cost = float(out.get("exact_cost_qcoords", math.nan))

    if fitness_stage == "approx":
        fitness = approx_quality
        residual = approx_residual
        selected = bool((residual > float(selection_threshold_qcoords)) and (fitness >= float(quality_threshold)))
    elif fitness_stage == "exact":
        # Exact |S| is the discovery criterion.  The approximate quality is used
        # only as an infinitesimal deterministic tie-breaker among candidates
        # with the same exact residual.
        fitness = exact_residual + float(exact_tie_breaker) * approx_quality
        residual = exact_residual
        selected = bool(exact_residual > float(selection_threshold_qcoords))
    else:
        raise ValueError(f"unknown fitness stage: {fitness_stage!r}")

    out.update(
        blind_fitness=float(fitness),
        blind_residual_qcoords=float(residual),
        blind_fitness_stage=str(fitness_stage),
        blind_primary_metric=("approx_quality_score" if fitness_stage == "approx" else "exact_residual_qcoords"),
        selected_blind=bool(selected),
        selection_threshold_qcoords=float(selection_threshold_qcoords),
        quality_threshold=float(quality_threshold),
        exact_tie_breaker=float(exact_tie_breaker),
    )
    # Convenience aliases used by plotting/analysis in both old and new runs.
    out.setdefault("approx_residual_qcoords", float(approx_residual))
    out.setdefault("exact_residual_qcoords", float(exact_residual))
    out.setdefault("exact_cost_qcoords", float(exact_cost))
    return out



def evaluate_candidate(
    ds: S.SCDCDiamond,
    thresholds: Iterable[float] = (0.45, 0.55, 0.65),
    initial_pool: InitialPool = "all",
    observer_init: ObserverInit = "zero",
    min_count: int = 1,
    require_mutual: bool = True,
    selection_threshold_qcoords: float = 1e-9,
    quality_threshold: float = 0.10,
    fitness_stage: FitnessStage = "approx",
    exact_tie_breaker: float = 1e-6,
) -> dict:
    """Evaluate one candidate under an approximate or exact selection stage.

    The function always computes both the approximate equalizer and the exact
    equalizer, so the CSV can audit approximate robustness even when the search
    is selected by exact ``log_q |S|``.
    """
    rows = _call_approx_equalizer_measure_compat(
        ds=ds,
        thresholds=thresholds,
        initial_pool=initial_pool,
        observer_init=observer_init,
        min_count=min_count,
        require_mutual=require_mutual,
    )
    if not rows:
        raise RuntimeError("approx_equalizer_measure returned no rows")
    norm_rows = [_normalize_approx_row(dict(r), width=int(ds.w)) for r in rows]
    best = max(
        norm_rows,
        key=lambda r: (
            float(r.get("approx_quality_score", -math.inf)),
            float(r.get("approx_residual_qcoords", -math.inf)),
            -float(r.get("approx_cross_mass_fraction", math.inf)),
        ),
    )
    out = dict(best)
    out.update(_exact_equalizer_metrics(ds, initial_pool=initial_pool, observer_init=observer_init))
    metrics = RS.interface_rule_diagnostics(ds)
    out.update(metrics)
    return _score_candidate(
        out,
        fitness_stage=fitness_stage,
        selection_threshold_qcoords=selection_threshold_qcoords,
        quality_threshold=quality_threshold,
        exact_tie_breaker=exact_tie_breaker,
    )


def make_random_candidate(
    kB: int,
    kM: int,
    kA: int,
    q: int,
    w: int,
    rng: np.random.Generator,
    extra_B: int | None = None,
    extra_M: int | None = None,
    extra_A: int | None = None,
) -> S.SCDCDiamond:
    ds = S.make_scdc_diamond(
        kB=kB,
        kC=kM,
        kD=kM,
        kA=kA,
        q=q,
        w=w,
        rng=rng,
        extra_B=extra_B,
        extra_C=extra_M,
        extra_D=extra_M,
        extra_A=extra_A,
    )
    ds.meta.update(rule_origin="blind_random")
    ds.joint.meta.update(ds.meta)
    return ds



