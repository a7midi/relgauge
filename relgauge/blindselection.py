"""
blindselection.py -- blind search for rules selected by interface consistency.

Purpose
-------
The rule-selection and approximate-equalizer experiments established two
separate facts:

* maximum-entropy random rule tables usually give a trivial shared interface
  alphabet, |S|=1;
* deliberately structured rules such as copy/permutive/block/canalizing rules
  support nontrivial exact or approximate shared interface labels.

Those structured ensembles are useful controls, but they are not yet a blind
selection test.  This module asks the sharper question:

    If the search objective only knows about interface consistency, does it
    discover nontrivial latent-interface rules?

The search does NOT select for copy accuracy, permutivity, block structure, or
canalization.  Those are reported only afterward as diagnostics.

Implemented search modes
------------------------
``null``
    Independent random-rule samples.  This calibrates the maximum-entropy null.

``evolve``
    A simple population search.  Candidates are random diamonds; descendants are
    generated by random rule-table mutations/resamplings.  Selection keeps the candidates with the largest declared fitness:
    approximate, exact, or a two-stage approximate-then-exact objective.

Fitness modes
-------------
``approx`` uses the approximate equalizer quality from ``approxequalizer.py``.
It is useful as a smooth search heuristic and mutation-robustness diagnostic,
but can be permissive on tiny alphabets.

``exact`` uses the exact interface equalizer residual ``log_q |S|`` from
``interfaceequalizer.py``.  This is the strict zero-error selection target.

``two_stage`` uses ``approx`` for a pretraining phase, then switches to
``exact``.  This lets the search climb a smoother landscape without making the
approximate equalizer the reported discovery criterion.

Interpretation
--------------
A positive blind-selection signal is not merely that a hand-designed copy rule
works.  It is:

    random null low  +  blind search high  +  post-hoc diagnostics show the
    winners have latent-label-preserving structure.

That is the computational form of the rule-selection hypothesis.
"""
