def _to_builtin(v):
    """Convert common numpy/pandas scalar values to pickle/JSON-friendly values."""
    if isinstance(v, (np.integer,)):
        return int(v)
    if isinstance(v, (np.floating,)):
        return float(v)
    if isinstance(v, (np.bool_,)):
        return bool(v)
    try:
        if pd.isna(v):
            return None
    except Exception:
        pass
    return v


def _metrics_snapshot(metrics: dict) -> dict:
    return {str(k): _to_builtin(v) for k, v in dict(metrics).items()}


def make_winner_record(ds: S.SCDCDiamond, metrics: dict) -> dict:
    """Return a self-contained pickle record for a selected candidate.

    The record stores a cloned ``SCDCDiamond`` object, not just CSV metrics, so
    later audit tools can recompute exact equalizers, source dependence,
    persistence, and mutation robustness.  The pickle is intended for local
    experiment artifacts only; do not load winner files from untrusted sources.
    """
    return {
        "format": "relgauge.blindwinner.v1",
        "diamond": clone_diamond(ds),
        "metrics": _metrics_snapshot(metrics),
    }


def _winner_key(record: dict) -> tuple[float, float, float, int]:
    m = record.get("metrics", {})
    return (
        float(m.get("exact_residual_qcoords") or 0.0),
        float(m.get("blind_fitness") or 0.0),
        float(m.get("approx_quality_score") or 0.0),
        int(m.get("generation") or 0),
    )


def _maybe_store_winner(
    store: list[dict] | None,
    ds: S.SCDCDiamond,
    metrics: dict,
    *,
    top_n: int = 20,
    min_exact_residual: float = 1e-9,
    include_null: bool = False,
) -> None:
    """Store candidate snapshots for later common-factor audit.

    By default we keep only evolved candidates whose exact residual is
    nontrivial.  The store is pruned online to the best ``top_n`` records by
    exact residual, blind fitness, approximate quality, and generation.
    """
    if store is None or int(top_n) <= 0:
        return
    if str(metrics.get("search_mode", "")) == "null" and not include_null:
        return
    exact = float(metrics.get("exact_residual_qcoords") or 0.0)
    if exact + 1e-12 < float(min_exact_residual):
        return
    store.append(make_winner_record(ds, metrics))
    # Keep a small buffer of near-best records so later generations can replace
    # earlier duplicates without retaining huge populations.
    max_keep = max(int(top_n), 1)
    if len(store) > 4 * max_keep:
        store.sort(key=_winner_key, reverse=True)
        del store[max_keep:]


def save_winner_records(path: str, records: list[dict], summary: dict | None = None) -> None:
    """Write winner records to a pickle file for ``commonfactoraudit``.

    Pickle is used deliberately because the rule tables and dataclass graph
    objects are numpy-heavy.  Treat these as local artifacts, not interchange
    files from untrusted sources.
    """
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    records = sorted(list(records), key=_winner_key, reverse=True)
    payload = {
        "format": "relgauge.blindwinner.bundle.v1",
        "n_records": int(len(records)),
        "summary": dict(summary or {}),
        "records": records,
    }
    with open(path, "wb") as f:
        pickle.dump(payload, f, protocol=pickle.HIGHEST_PROTOCOL)


def load_winner_records(path: str) -> list[dict]:
    """Load records written by ``save_winner_records``."""
    with open(path, "rb") as f:
        payload = pickle.load(f)
    if isinstance(payload, dict) and "records" in payload:
        return list(payload["records"])
    if isinstance(payload, list):
        return payload
    raise ValueError(f"unrecognized winner bundle format in {path!r}")


