def run_null_sample(
    kB: int = 2,
    kM: int = 2,
    kA: int = 2,
    w: int = 1,
    q: int = 3,
    n_samples: int = 80,
    thresholds: Iterable[float] = (0.45, 0.55, 0.65),
    initial_pool: InitialPool = "all",
    observer_init: ObserverInit = "zero",
    min_count: int = 1,
    require_mutual: bool = True,
    selection_threshold_qcoords: float = 1e-9,
    quality_threshold: float = 0.10,
    fitness_mode: FitnessMode = "approx",
    exact_tie_breaker: float = 1e-6,
    extra_B: int | None = None,
    extra_M: int | None = None,
    extra_A: int | None = None,
    base_seed: int = 0,
) -> pd.DataFrame:
    rows: list[dict] = []
    for i in range(int(n_samples)):
        seed = (int(base_seed) * 1000003 + int(i) * 9176 + q * 17 + kA * 131 + w * 19) % 2**32
        rng = np.random.default_rng(seed)
        ds = make_random_candidate(kB, kM, kA, q, w, rng, extra_B=extra_B, extra_M=extra_M, extra_A=extra_A)
        m = evaluate_candidate(
            ds,
            thresholds=thresholds,
            initial_pool=initial_pool,
            observer_init=observer_init,
            min_count=min_count,
            require_mutual=require_mutual,
            selection_threshold_qcoords=selection_threshold_qcoords,
            quality_threshold=quality_threshold,
            fitness_stage="exact" if fitness_mode in ("exact", "two_stage") else "approx",
            exact_tie_breaker=exact_tie_breaker,
        )
        m.update(
            search_mode="null",
            run=-1,
            generation=0,
            candidate=i,
            seed=int(seed),
            parent_candidate=-1,
            mutation_vertices="[]",
            entry_mutations=0,
            table_mutations=0,
            target_mode="none",
            proposal_mode="none",
            fitness_mode=str(fitness_mode),
            pretrain_generations=0,
        )
        rows.append(m)
    return pd.DataFrame(rows)


def run_evolution(
    kB: int = 2,
    kM: int = 2,
    kA: int = 2,
    w: int = 1,
    q: int = 3,
    n_runs: int = 5,
    population: int = 16,
    generations: int = 20,
    elite_fraction: float = 0.25,
    immigrant_fraction: float = 0.10,
    target: TargetMode = "interface",
    proposal_mode: ProposalMode = "mixed",
    entry_rate: float = 0.08,
    table_rate: float = 0.10,
    n_table_mutations: int = 1,
    thresholds: Iterable[float] = (0.45, 0.55, 0.65),
    initial_pool: InitialPool = "all",
    observer_init: ObserverInit = "zero",
    min_count: int = 1,
    require_mutual: bool = True,
    selection_threshold_qcoords: float = 1e-9,
    quality_threshold: float = 0.10,
    fitness_mode: FitnessMode = "approx",
    pretrain_generations: int | None = None,
    exact_tie_breaker: float = 1e-6,
    extra_B: int | None = None,
    extra_M: int | None = None,
    extra_A: int | None = None,
    base_seed: int = 0,
    verbose: bool = True,
    winner_store: list[dict] | None = None,
    winner_top_n: int = 20,
    winner_min_exact_residual: float = 1e-9,
) -> pd.DataFrame:
    rows: list[dict] = []
    population = max(2, int(population))
    n_runs = max(1, int(n_runs))
    generations = max(0, int(generations))
    n_elite = max(1, int(math.ceil(population * float(elite_fraction))))
    n_imm = max(0, int(round(population * float(immigrant_fraction))))
    if fitness_mode not in ("approx", "exact", "two_stage"):
        raise ValueError(f"unknown fitness_mode: {fitness_mode!r}")
    if pretrain_generations is None or int(pretrain_generations) < 0:
        pretrain_generations = generations // 2
    pretrain_generations = max(0, min(int(pretrain_generations), int(generations)))

    for run in range(n_runs):
        rng = np.random.default_rng((int(base_seed) * 99173 + run * 1009 + q * 37 + kA * 101 + w) % 2**32)
        pop: list[S.SCDCDiamond] = [
            make_random_candidate(kB, kM, kA, q, w, rng, extra_B=extra_B, extra_M=extra_M, extra_A=extra_A)
            for _ in range(population)
        ]
        evals: list[tuple[float, S.SCDCDiamond, dict]] = []
        candidate_counter = 0
        for gen in range(generations + 1):
            evals = []
            for ci, ds in enumerate(pop):
                m = evaluate_candidate(
                    ds,
                    thresholds=thresholds,
                    initial_pool=initial_pool,
                    observer_init=observer_init,
                    min_count=min_count,
                    require_mutual=require_mutual,
                    selection_threshold_qcoords=selection_threshold_qcoords,
                    quality_threshold=quality_threshold,
                    fitness_stage=("approx" if fitness_mode == "approx" or (fitness_mode == "two_stage" and gen < pretrain_generations) else "exact"),
                    exact_tie_breaker=exact_tie_breaker,
                )
                m.update(
                    search_mode="evolve",
                    run=int(run),
                    generation=int(gen),
                    candidate=int(candidate_counter),
                    seed=int(base_seed),
                    parent_candidate=int(ds.meta.get("parent_candidate", -1)),
                    mutation_vertices=json.dumps(ds.meta.get("mutation_vertices", [])),
                    entry_mutations=int(ds.meta.get("entry_mutations", 0)),
                    table_mutations=int(ds.meta.get("table_mutations", 0)),
                    target_mode=str(target),
                    proposal_mode=str(proposal_mode),
                    fitness_mode=str(fitness_mode),
                    pretrain_generations=int(pretrain_generations),
                )
                candidate_counter += 1
                _maybe_store_winner(
                    winner_store,
                    ds,
                    m,
                    top_n=winner_top_n,
                    min_exact_residual=winner_min_exact_residual,
                    include_null=False,
                )
                rows.append(m)
                evals.append((float(m["blind_fitness"]), ds, m))
            evals.sort(key=lambda x: x[0], reverse=True)
            if verbose:
                best = evals[0][2]
                print(
                    f"blind-evolve run={run} gen={gen} stage={best.get('blind_fitness_stage','?')} "
                    f"best_fitness={best['blind_fitness']:.4f} "
                    f"exact={best.get('exact_residual_qcoords', math.nan):.4f} "
                    f"approx={best.get('approx_residual_qcoords', math.nan):.4f} selected={best['selected_blind']}",
                    flush=True,
                )
            if gen >= generations:
                break

            elites = [clone_diamond(ds) for _fit, ds, _m in evals[:n_elite]]
            next_pop: list[S.SCDCDiamond] = []
            # Preserve exact elite candidates.
            next_pop.extend(clone_diamond(e) for e in elites)
            # Add random immigrants.
            for _ in range(min(n_imm, population - len(next_pop))):
                next_pop.append(make_random_candidate(kB, kM, kA, q, w, rng, extra_B=extra_B, extra_M=extra_M, extra_A=extra_A))
            # Fill the rest with mutated elite descendants.
            while len(next_pop) < population:
                parent_idx = int(rng.integers(0, len(elites)))
                parent = elites[parent_idx]
                child, info = mutate_diamond(
                    parent,
                    rng,
                    target=target,
                    proposal_mode=proposal_mode,
                    entry_rate=entry_rate,
                    table_rate=table_rate,
                    n_table_mutations=n_table_mutations,
                    ensure_change=True,
                )
                child.meta.update(
                    parent_candidate=int(evals[parent_idx][2].get("candidate", -1)),
                    entry_mutations=int(info["entry_mutations"]),
                    table_mutations=int(info["table_mutations"]),
                    mutation_vertices=json.loads(info["mutation_vertices"]),
                )
                child.joint.meta.update(child.meta)
                next_pop.append(child)
            pop = next_pop[:population]
    return pd.DataFrame(rows)


def run_blind_selection(
    ks_B: Iterable[int] = (2,),
    ks_M: Iterable[int] = (2,),
    ks_A: Iterable[int] = (2,),
    ws: Iterable[int] = (1,),
    q: int = 3,
    null_samples: int = 80,
    n_runs: int = 5,
    population: int = 16,
    generations: int = 20,
    elite_fraction: float = 0.25,
    immigrant_fraction: float = 0.10,
    target: TargetMode = "interface",
    proposal_mode: ProposalMode = "mixed",
    entry_rate: float = 0.08,
    table_rate: float = 0.10,
    n_table_mutations: int = 1,
    thresholds: Iterable[float] = (0.45, 0.55, 0.65),
    initial_pool: InitialPool = "all",
    observer_init: ObserverInit = "zero",
    min_count: int = 1,
    require_mutual: bool = True,
    selection_threshold_qcoords: float = 1e-9,
    quality_threshold: float = 0.10,
    fitness_mode: FitnessMode = "approx",
    pretrain_generations: int | None = None,
    exact_tie_breaker: float = 1e-6,
    extra_B: int | None = None,
    extra_M: int | None = None,
    extra_A: int | None = None,
    base_seed: int = 0,
    max_joint_states: int = 4 ** 9,
    verbose: bool = True,
    winner_store: list[dict] | None = None,
    winner_top_n: int = 20,
    winner_min_exact_residual: float = 1e-9,
) -> pd.DataFrame:
    rows: list[pd.DataFrame] = []
    for kB in ks_B:
        for kM in ks_M:
            for kA in ks_A:
                if q ** (int(kB) + 2 * int(kM) + int(kA)) > int(max_joint_states):
                    if verbose:
                        print(f"skip kB={kB}, kM={kM}, kA={kA}: joint state space too large", flush=True)
                    continue
                for w in ws:
                    if int(w) > min(int(kB), int(kM)) or int(kA) < 2 * int(w):
                        continue
                    if int(null_samples) > 0:
                        nd = run_null_sample(
                            kB=int(kB), kM=int(kM), kA=int(kA), w=int(w), q=int(q),
                            n_samples=int(null_samples), thresholds=thresholds,
                            initial_pool=initial_pool, observer_init=observer_init,
                            min_count=min_count, require_mutual=require_mutual,
                            selection_threshold_qcoords=selection_threshold_qcoords,
                            quality_threshold=quality_threshold,
                            fitness_mode=fitness_mode, exact_tie_breaker=exact_tie_breaker,
                            extra_B=extra_B, extra_M=extra_M, extra_A=extra_A,
                            base_seed=base_seed + int(kA) * 31 + int(w),
                        )
                        rows.append(nd)
                        if verbose:
                            print(f"blind-null kB={kB}, kM={kM}, kA={kA}, w={w} done", flush=True)
                    if int(n_runs) > 0 and int(population) > 0:
                        ed = run_evolution(
                            kB=int(kB), kM=int(kM), kA=int(kA), w=int(w), q=int(q),
                            n_runs=int(n_runs), population=int(population), generations=int(generations),
                            elite_fraction=elite_fraction, immigrant_fraction=immigrant_fraction,
                            target=target, proposal_mode=proposal_mode,
                            entry_rate=entry_rate, table_rate=table_rate, n_table_mutations=n_table_mutations,
                            thresholds=thresholds, initial_pool=initial_pool, observer_init=observer_init,
                            min_count=min_count, require_mutual=require_mutual,
                            selection_threshold_qcoords=selection_threshold_qcoords,
                            quality_threshold=quality_threshold,
                            fitness_mode=fitness_mode, pretrain_generations=pretrain_generations,
                            exact_tie_breaker=exact_tie_breaker,
                            extra_B=extra_B, extra_M=extra_M, extra_A=extra_A,
                            base_seed=base_seed + int(kA) * 157 + int(w) * 11,
                            verbose=verbose,
                            winner_store=winner_store,
                            winner_top_n=winner_top_n,
                            winner_min_exact_residual=winner_min_exact_residual,
                        )
                        rows.append(ed)
                        if verbose:
                            print(f"blind-evolve kB={kB}, kM={kM}, kA={kA}, w={w} done", flush=True)
    if not rows:
        return pd.DataFrame()
    return pd.concat(rows, ignore_index=True)


