def clone_diamond(ds: S.SCDCDiamond) -> S.SCDCDiamond:
    """Deep-copy the mutable rule tables of a diamond while keeping topology.

    The SCC skeleton systems ``B``, ``Csys``, ``Dsys``, and ``Askel`` are used
    only for recurrent-pool construction.  Blind search defaults to
    ``initial_pool='all'``; for that physical run the joint system is the object
    that matters.  We still preserve the skeleton references so the dataclass
    remains fully compatible with ``scdcdiamond`` helpers.
    """
    joint = C.RelationalSystem(
        ds.joint.k,
        ds.joint.q,
        [tuple(p) for p in ds.joint.preds],
        [np.asarray(r, dtype=np.int64).copy() for r in ds.joint.rules],
        meta=dict(ds.joint.meta),
    )
    return S.SCDCDiamond(
        B=ds.B,
        Csys=ds.Csys,
        Dsys=ds.Dsys,
        Askel=ds.Askel,
        joint=joint,
        kB=ds.kB,
        kC=ds.kC,
        kD=ds.kD,
        kA=ds.kA,
        q=ds.q,
        w=ds.w,
        offB=ds.offB,
        offC=ds.offC,
        offD=ds.offD,
        offA=ds.offA,
        interface_BC=tuple(ds.interface_BC),
        interface_BD=tuple(ds.interface_BD),
        interface_CA=tuple(ds.interface_CA),
        interface_DA=tuple(ds.interface_DA),
        meta=dict(ds.meta),
    )


def target_vertices(ds: S.SCDCDiamond, target: TargetMode = "interface") -> tuple[int, ...]:
    """Vertices whose rule tables are allowed to mutate.

    ``interface`` is the practical blind-search default: it mutates the rules on
    the declared B -> {C,D} -> A interface pipeline, but does not encode a copy,
    block, or permutation objective.  ``all`` is the stricter full-rule search.
    """
    if target == "all":
        return tuple(range(ds.k_total))
    if target == "source":
        return tuple(ds.offB + j for j in range(ds.w))
    branch: list[int] = []
    branch.extend(t for _s, t in ds.interface_BC)
    branch.extend(t for _s, t in ds.interface_BD)
    if target == "branch":
        return tuple(sorted(set(int(x) for x in branch)))
    sink: list[int] = []
    sink.extend(t for _s, t in ds.interface_CA)
    sink.extend(t for _s, t in ds.interface_DA)
    if target == "sink":
        return tuple(sorted(set(int(x) for x in sink)))
    if target == "interface":
        out: list[int] = []
        out.extend(ds.offB + j for j in range(ds.w))
        out.extend(branch)
        out.extend(sink)
        return tuple(sorted(set(int(x) for x in out)))
    raise ValueError(f"unknown target mode: {target!r}")


def _random_rule(q: int, indeg: int, rng: np.random.Generator) -> np.ndarray:
    return rng.integers(0, int(q), size=int(q) ** int(indeg), dtype=np.int64)


def _mutate_entries(rule: np.ndarray, q: int, rate: float, rng: np.random.Generator) -> tuple[np.ndarray, int]:
    out = np.asarray(rule, dtype=np.int64).copy()
    if out.size == 0 or rate <= 0:
        return out, 0
    mask = rng.random(out.size) < float(rate)
    n = int(mask.sum())
    if n <= 0:
        return out, 0
    jumps = rng.integers(1, int(q), size=n, dtype=np.int64)
    out[mask] = (out[mask] + jumps) % int(q)
    return out, n


def mutate_diamond(
    ds: S.SCDCDiamond,
    rng: np.random.Generator,
    target: TargetMode = "interface",
    proposal_mode: ProposalMode = "mixed",
    entry_rate: float = 0.08,
    table_rate: float = 0.10,
    n_table_mutations: int = 1,
    ensure_change: bool = True,
) -> tuple[S.SCDCDiamond, dict]:
    """Return a mutated clone and a mutation diagnostic dict.

    Selection never sees copy/block/permutation labels.  It only receives the
    approximate equalizer score after the mutation is evaluated.
    """
    child = clone_diamond(ds)
    verts = list(target_vertices(child, target))
    if not verts:
        return child, {"mutation_vertices": "[]", "entry_mutations": 0, "table_mutations": 0}

    entry_changes = 0
    table_changes = 0
    changed_vertices: set[int] = set()

    if proposal_mode not in ("entry", "table", "mixed"):
        raise ValueError(f"unknown proposal mode: {proposal_mode!r}")

    do_table = proposal_mode in ("table", "mixed")
    do_entry = proposal_mode in ("entry", "mixed")

    # Whole-table resampling moves allow escape from local maxima; entry moves
    # give smooth mutation/robustness exploration.
    if do_table and table_rate > 0:
        n_try = max(0, int(n_table_mutations))
        if proposal_mode == "mixed":
            n_try = max(n_try, int(rng.random() < float(table_rate)))
        for _ in range(n_try):
            if proposal_mode == "table" or rng.random() < float(table_rate):
                v = int(rng.choice(verts))
                child.joint.rules[v] = _random_rule(child.q, len(child.joint.preds[v]), rng)
                table_changes += 1
                changed_vertices.add(v)

    if do_entry and entry_rate > 0:
        for v in verts:
            new_rule, n = _mutate_entries(child.joint.rules[v], child.q, entry_rate, rng)
            if n:
                child.joint.rules[v] = new_rule
                entry_changes += int(n)
                changed_vertices.add(int(v))

    if ensure_change and entry_changes == 0 and table_changes == 0:
        v = int(rng.choice(verts))
        r = child.joint.rules[v].copy()
        if r.size:
            idx = int(rng.integers(0, r.size))
            jump = int(rng.integers(1, child.q))
            r[idx] = (int(r[idx]) + jump) % child.q
            child.joint.rules[v] = r
            entry_changes += 1
            changed_vertices.add(v)

    child.meta.update(
        blind_target=str(target),
        blind_proposal_mode=str(proposal_mode),
        entry_rate=float(entry_rate),
        table_rate=float(table_rate),
        mutation_vertices=sorted(int(v) for v in changed_vertices),
    )
    child.joint.meta.update(child.meta)
    return child, {
        "mutation_vertices": json.dumps(sorted(int(v) for v in changed_vertices)),
        "entry_mutations": int(entry_changes),
        "table_mutations": int(table_changes),
    }


