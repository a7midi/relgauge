def _best_per_generation(df: pd.DataFrame) -> pd.DataFrame:
    if df.empty or "generation" not in df.columns:
        return pd.DataFrame()
    evo = df[df["search_mode"] == "evolve"].copy()
    if evo.empty:
        return evo
    idx = evo.groupby(["kB", "kM", "kA", "w", "run", "generation"], dropna=False)["blind_fitness"].idxmax()
    return evo.loc[idx].copy()


def analyze_blind_selection(df: pd.DataFrame, top_n: int = 10) -> dict:
    if df.empty:
        return {"verdict": "NO DATA"}
    eps = 1e-9
    # New runs have both exact and approximate metrics; old runs may only have
    # approximate.  Audit whichever columns are present.
    residual_col = "blind_residual_qcoords" if "blind_residual_qcoords" in df.columns else "approx_residual_qcoords"
    viol = 0
    if "approx_residual_qcoords" in df:
        viol += int((df["approx_residual_qcoords"] < -eps).sum())
        viol += int((df["approx_residual_qcoords"] > df["w"].astype(float) + eps).sum())
    if "exact_residual_qcoords" in df:
        viol += int((df["exact_residual_qcoords"] < -eps).sum())
        viol += int((df["exact_residual_qcoords"] > df["w"].astype(float) + eps).sum())
    if "approx_quality_score" in df:
        viol += int((df["approx_quality_score"] < -eps).sum())
    viol += int((df["blind_fitness"] < -eps).sum())

    null = df[df["search_mode"] == "null"]
    best_gen = _best_per_generation(df)
    final = pd.DataFrame()
    if not best_gen.empty:
        max_gen = best_gen.groupby(["kB", "kM", "kA", "w", "run"], dropna=False)["generation"].transform("max")
        final = best_gen[best_gen["generation"] == max_gen]

    def _safe_mean(s):
        return float(s.mean()) if len(s) else math.nan

    def _best_val(frame: pd.DataFrame, value_col: str, score_col: str = "blind_fitness"):
        if frame.empty or value_col not in frame.columns:
            return math.nan
        idx = frame[score_col].idxmax()
        return float(frame.loc[idx, value_col])

    null_sel = _safe_mean(null["selected_blind"]) if len(null) else math.nan
    null_best = float(null["blind_fitness"].max()) if len(null) else math.nan
    null_best_res = _best_val(null, residual_col)
    null_best_exact = _best_val(null, "exact_residual_qcoords")
    null_best_approx = _best_val(null, "approx_residual_qcoords")

    final_sel = _safe_mean(final["selected_blind"]) if len(final) else math.nan
    final_best = float(final["blind_fitness"].max()) if len(final) else math.nan
    final_best_res = _best_val(final, residual_col)
    final_best_exact = _best_val(final, "exact_residual_qcoords")
    final_best_approx = _best_val(final, "approx_residual_qcoords")

    all_best = float(df["blind_fitness"].max())
    all_best_res = _best_val(df, residual_col)
    all_best_exact = _best_val(df, "exact_residual_qcoords")
    all_best_approx = _best_val(df, "approx_residual_qcoords")

    selected = df[df["selected_blind"]]
    unselected = df[~df["selected_blind"]]
    selected_mi = _safe_mean(selected["rule_interface_mi_norm_mean"]) if "rule_interface_mi_norm_mean" in selected else math.nan
    unselected_mi = _safe_mean(unselected["rule_interface_mi_norm_mean"]) if "rule_interface_mi_norm_mean" in unselected else math.nan
    selected_acc = _safe_mean(selected["rule_interface_copy_accuracy_mean"]) if "rule_interface_copy_accuracy_mean" in selected else math.nan
    unselected_acc = _safe_mean(unselected["rule_interface_copy_accuracy_mean"]) if "rule_interface_copy_accuracy_mean" in unselected else math.nan

    progression = []
    if not best_gen.empty:
        agg = dict(
            best_fitness=("blind_fitness", "max"),
            mean_best_fitness=("blind_fitness", "mean"),
            selected_fraction=("selected_blind", "mean"),
            best_residual=(residual_col, "max"),
            mean_interface_mi=("rule_interface_mi_norm_mean", "mean"),
        )
        if "exact_residual_qcoords" in best_gen.columns:
            agg["best_exact_residual"] = ("exact_residual_qcoords", "max")
        if "approx_residual_qcoords" in best_gen.columns:
            agg["best_approx_residual"] = ("approx_residual_qcoords", "max")
        if "blind_fitness_stage" in best_gen.columns:
            # store the modal stage in a small post-processing step below
            pass
        g = best_gen.groupby("generation").agg(**agg)
        for gen, r in g.iterrows():
            item = dict(
                generation=int(gen),
                best_fitness=float(r.best_fitness),
                mean_best_fitness=float(r.mean_best_fitness),
                selected_fraction=float(r.selected_fraction),
                best_residual=float(r.best_residual),
                mean_interface_mi=float(r.mean_interface_mi),
            )
            if "best_exact_residual" in g.columns:
                item["best_exact_residual"] = float(r.best_exact_residual)
            if "best_approx_residual" in g.columns:
                item["best_approx_residual"] = float(r.best_approx_residual)
            if "blind_fitness_stage" in best_gen.columns:
                stages = best_gen[best_gen.generation == gen]["blind_fitness_stage"].astype(str)
                item["stage"] = stages.mode().iloc[0] if len(stages) else ""
            progression.append(item)

    top = df.sort_values("blind_fitness", ascending=False).head(int(top_n))
    top_rows = []
    cols = [
        "search_mode", "run", "generation", "candidate", "blind_fitness", "blind_residual_qcoords",
        "blind_fitness_stage", "blind_primary_metric", "selected_blind", "exact_residual_qcoords",
        "exact_shared_classes_mean", "exact_cost_qcoords", "exact_edge_density_mean",
        "approx_residual_qcoords", "approx_quality_score", "approx_inlier_mass_fraction",
        "approx_cross_mass_fraction", "approx_shared_classes", "approx_threshold",
        "rule_interface_mi_norm_mean", "rule_interface_copy_accuracy_mean",
        "rule_interface_cond_entropy_bits_mean", "target_mode", "proposal_mode", "fitness_mode",
    ]
    for _, r in top.iterrows():
        row = {}
        for c in cols:
            if c not in r:
                continue
            v = r[c]
            if isinstance(v, (np.floating, float)):
                row[c] = float(v)
            elif isinstance(v, (np.integer, int)) and c not in ("search_mode", "target_mode", "proposal_mode", "fitness_mode", "blind_fitness_stage", "blind_primary_metric"):
                row[c] = int(v)
            else:
                row[c] = v
        top_rows.append(row)

    improvement = None
    if not math.isnan(null_best) and not math.isnan(final_best):
        improvement = float(final_best - null_best)

    # Exact/two-stage success can occur without selected fraction beating the
    # null fraction by 0.2 if the null is calibrated under approximate fitness.
    exact_gain = None
    if not math.isnan(null_best_exact) and not math.isnan(final_best_exact):
        exact_gain = float(final_best_exact - null_best_exact)

    if viol:
        verdict = "IMPLEMENTATION WARNING: blind-selection bounds violated"
    elif (not math.isnan(final_best_exact) and final_best_exact > 1e-9 and (math.isnan(null_best_exact) or final_best_exact > null_best_exact + 1e-9)):
        verdict = "EXACT BLIND-SELECTION SIGNAL: search finds nontrivial exact interface alphabets above the random null"
    elif not math.isnan(final_sel) and final_sel > max(0.20, (0.0 if math.isnan(null_sel) else null_sel + 0.20)) and (math.isnan(null_best) or final_best > null_best + 0.05):
        verdict = "BLIND-SELECTION SIGNAL: consistency search finds nontrivial latent-interface candidates above the random null"
    elif len(selected) > 0 and (math.isnan(null_sel) or _safe_mean(selected["search_mode"].eq("evolve")) > 0):
        verdict = "RARE / PARTIAL BLIND HITS: inspect winners for fine-tuning versus structure"
    else:
        verdict = "NO BLIND-SELECTION SIGNAL in this search budget"

    fitness_modes = sorted(str(x) for x in df.get("fitness_mode", pd.Series([], dtype=str)).dropna().unique())
    return dict(
        verdict=verdict,
        blind_selection_bound_violations=int(viol),
        n_rows=int(len(df)),
        fitness_modes=fitness_modes,
        null_selection_fraction=None if math.isnan(null_sel) else float(null_sel),
        null_best_fitness=None if math.isnan(null_best) else float(null_best),
        null_best_residual_qcoords=None if math.isnan(null_best_res) else float(null_best_res),
        null_best_exact_residual_qcoords=None if math.isnan(null_best_exact) else float(null_best_exact),
        null_best_approx_residual_qcoords=None if math.isnan(null_best_approx) else float(null_best_approx),
        evolved_final_best_selection_fraction=None if math.isnan(final_sel) else float(final_sel),
        evolved_final_best_fitness=None if math.isnan(final_best) else float(final_best),
        evolved_final_best_residual_qcoords=None if math.isnan(final_best_res) else float(final_best_res),
        evolved_final_best_exact_residual_qcoords=None if math.isnan(final_best_exact) else float(final_best_exact),
        evolved_final_best_approx_residual_qcoords=None if math.isnan(final_best_approx) else float(final_best_approx),
        all_time_best_fitness=float(all_best),
        all_time_best_residual_qcoords=float(all_best_res),
        all_time_best_exact_residual_qcoords=None if math.isnan(all_best_exact) else float(all_best_exact),
        all_time_best_approx_residual_qcoords=None if math.isnan(all_best_approx) else float(all_best_approx),
        evolved_improvement_over_null_best=improvement,
        evolved_exact_residual_improvement_over_null=exact_gain,
        mean_selected_interface_mi_norm=None if math.isnan(selected_mi) else float(selected_mi),
        mean_unselected_interface_mi_norm=None if math.isnan(unselected_mi) else float(unselected_mi),
        mean_selected_copy_accuracy=None if math.isnan(selected_acc) else float(selected_acc),
        mean_unselected_copy_accuracy=None if math.isnan(unselected_acc) else float(unselected_acc),
        best_by_generation=progression,
        top_candidates=top_rows,
    )


def plot_blind_selection(df: pd.DataFrame, path: str) -> None:
    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    if df.empty:
        raise ValueError("empty dataframe")
    best_gen = _best_per_generation(df)
    fig, ax = plt.subplots(1, 3, figsize=(15, 4))

    if not best_gen.empty:
        g = best_gen.groupby("generation")["blind_fitness"].agg(["mean", "max"])
        ax[0].plot(g.index, g["mean"], "o-", label="mean run-best")
        ax[0].plot(g.index, g["max"], "s--", label="global best")
        if "exact_residual_qcoords" in best_gen.columns:
            ge = best_gen.groupby("generation")["exact_residual_qcoords"].max()
            ax[0].plot(ge.index, ge.values, "^-", label="best exact |S|")
    null = df[df.search_mode == "null"]
    if len(null):
        ax[0].axhline(null.blind_fitness.max(), linestyle=":", label="null best")
    ax[0].set_xlabel("generation")
    ax[0].set_ylabel("blind fitness")
    ax[0].set_title("fitness selected by consistency")
    ax[0].legend(fontsize=7)

    if not best_gen.empty:
        gsel = best_gen.groupby("generation")["selected_blind"].mean()
        ax[1].plot(gsel.index, gsel.values, "o-")
    ax[1].set_xlabel("generation")
    ax[1].set_ylabel("selected fraction among run-bests")
    ax[1].set_ylim(-0.05, 1.05)
    ax[1].set_title("blind selection over time")

    ax[2].scatter(df["rule_interface_mi_norm_mean"], df["blind_fitness"], s=14, alpha=0.55)
    ax[2].set_xlabel("post-hoc interface-rule MI / log q")
    ax[2].set_ylabel("blind fitness")
    ax[2].set_title("post-hoc structure of blind winners")

    fig.tight_layout()
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    fig.savefig(path, dpi=130)
    plt.close(fig)


