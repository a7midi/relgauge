"""
algebraaudit.py -- extract the finite algebra carried by selected interfaces.

Stage-1 audit for the selected common-factor program.

The blind-selection run can save exact equalizer winners via --save-winners.
Those winners contain the actual local rule tables.  This module loads the
winner bundle and extracts the algebra generated by interface-local
permutations.

For an interface edge u -> v, the target rule at v is a table

    lambda_v(..., x_u, ...) -> x_v'

For every assignment of the non-interface inputs to v, vary only x_u in
[0, q).  This gives a function f_context : [q] -> [q].  If f_context is a
bijection, it is a permutation of the interface alphabet.  The permutations
seen across all interface edges and all internal contexts generate a finite
permutation group.

Interpretation
--------------
  * C_q / Z_q-like, transitive cyclic group: finite phase candidate.
  * V4, D4, S4, etc.: finite label/gauge-like symmetry, but not U(1)-like.
  * trivial or few permutations: selected exact S exists, but the local rule
    tables are not permutive on the raw interface alphabet.

This is an analysis pass on saved winners, not a new topology experiment.
Winner pickle files are local artifacts; do not load them from untrusted
sources.
"""
