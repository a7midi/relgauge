# --------------------------------------------------------------------------- #
# Small permutation group utilities
# --------------------------------------------------------------------------- #
def identity_perm(q: int) -> tuple[int, ...]:
    return tuple(range(int(q)))


def compose_perm(p: tuple[int, ...], r: tuple[int, ...]) -> tuple[int, ...]:
    """Composition p ∘ r, i.e. apply r first, then p."""
    return tuple(p[i] for i in r)


def inverse_perm(p: tuple[int, ...]) -> tuple[int, ...]:
    inv = [0] * len(p)
    for i, j in enumerate(p):
        inv[int(j)] = int(i)
    return tuple(inv)


def perm_power(p: tuple[int, ...], n: int) -> tuple[int, ...]:
    out = identity_perm(len(p))
    for _ in range(int(n)):
        out = compose_perm(p, out)
    return out


def perm_order(p: tuple[int, ...]) -> int:
    e = identity_perm(len(p))
    x = e
    for n in range(1, math.factorial(len(p)) + 1):
        x = compose_perm(p, x)
        if x == e:
            return n
    return -1


def perm_parity(p: tuple[int, ...]) -> int:
    inv = 0
    for i in range(len(p)):
        for j in range(i + 1, len(p)):
            if p[i] > p[j]:
                inv += 1
    return inv % 2


def generated_group(generators: Iterable[tuple[int, ...]], q: int) -> set[tuple[int, ...]]:
    """Return the subgroup of S_q generated by the given permutations."""
    q = int(q)
    gens = {tuple(map(int, g)) for g in generators if len(tuple(g)) == q}
    gens = {g for g in gens if sorted(g) == list(range(q))}
    if not gens:
        return {identity_perm(q)}
    gens |= {inverse_perm(g) for g in list(gens)}
    e = identity_perm(q)
    group: set[tuple[int, ...]] = {e}
    queue: deque[tuple[int, ...]] = deque([e])
    while queue:
        h = queue.popleft()
        for g in gens:
            for new in (compose_perm(g, h), compose_perm(h, g)):
                if new not in group:
                    group.add(new)
                    queue.append(new)
                    # Safety: cannot exceed |S_q|.
                    if len(group) > math.factorial(q):
                        raise RuntimeError("permutation closure exceeded S_q")
    return group


def _cyclic_generated_by(p: tuple[int, ...]) -> set[tuple[int, ...]]:
    q = len(p)
    out = {identity_perm(q)}
    x = identity_perm(q)
    for _ in range(1, math.factorial(q) + 1):
        x = compose_perm(p, x)
        out.add(x)
        if x == identity_perm(q):
            break
    return out


def is_abelian(group: set[tuple[int, ...]]) -> bool:
    gl = list(group)
    for a in gl:
        for b in gl:
            if compose_perm(a, b) != compose_perm(b, a):
                return False
    return True


def is_cyclic(group: set[tuple[int, ...]]) -> bool:
    return any(_cyclic_generated_by(g) == group for g in group)


def action_orbits(group: set[tuple[int, ...]], q: int) -> list[list[int]]:
    unseen = set(range(int(q)))
    orbits: list[list[int]] = []
    while unseen:
        s = min(unseen)
        orb = {g[s] for g in group}
        changed = True
        while changed:
            changed = False
            for x in list(orb):
                for g in group:
                    if g[x] not in orb:
                        orb.add(g[x])
                        changed = True
        orbits.append(sorted(orb))
        unseen -= orb
    return orbits


def perm_to_cycles(p: tuple[int, ...]) -> str:
    seen = [False] * len(p)
    cycles = []
    for i in range(len(p)):
        if seen[i]:
            continue
        cur = []
        j = i
        while not seen[j]:
            seen[j] = True
            cur.append(j)
            j = p[j]
        if len(cur) > 1:
            cycles.append("(" + " ".join(map(str, cur)) + ")")
    return "".join(cycles) if cycles else "()"


def classify_group(group: set[tuple[int, ...]], q: int) -> dict:
    q = int(q)
    order = len(group)
    abelian = is_abelian(group)
    cyclic = is_cyclic(group)
    all_even = all(perm_parity(g) == 0 for g in group)
    orbits = action_orbits(group, q)
    transitive = len(orbits) == 1
    elem_orders = Counter(perm_order(g) for g in group)

    name = f"subgroup_order_{order}"
    if order == 1:
        name = "trivial"
    elif cyclic:
        name = f"C{order}"
    # Common noncyclic subgroups of S4/S5; these names are structural hints.
    if q == 4:
        if order == 4 and abelian and not cyclic:
            name = "V4"
        elif order == 6 and not abelian:
            name = "S3"
        elif order == 8 and not abelian:
            name = "D4"
        elif order == 12 and all_even:
            name = "A4"
        elif order == 24:
            name = "S4"
    elif q == 5:
        if order == 5 and cyclic and transitive:
            name = "C5/Z5"
        elif order == 10 and not abelian:
            name = "D5"
        elif order == 20 and not abelian and transitive:
            name = "AGL(1,5)-like"
        elif order == 60 and all_even:
            name = "A5"
        elif order == 120:
            name = "S5"

    zq_like = bool(cyclic and transitive and order == q)
    symmetric = bool(order == math.factorial(q))
    return dict(
        group_order=int(order),
        group_name=name,
        group_abelian=bool(abelian),
        group_cyclic=bool(cyclic),
        group_transitive=bool(transitive),
        group_all_even=bool(all_even),
        group_zq_like=bool(zq_like),
        group_symmetric=bool(symmetric),
        group_orbits=json.dumps(orbits),
        group_element_order_hist=json.dumps({int(k): int(v) for k, v in sorted(elem_orders.items())}),
    )


# --------------------------------------------------------------------------- #
# Local rule restrictions along interface edges
# --------------------------------------------------------------------------- #
def _rule_index(vals: Iterable[int], q: int) -> int:
    idx = 0
    p = 1
    for v in vals:
        idx += int(v) * p
        p *= int(q)
    return int(idx)


def _context_rows(n: int, q: int):
    if n <= 0:
        yield ()
    else:
        yield from itertools.product(range(int(q)), repeat=int(n))


def restricted_functions_for_edge(ds: S.SCDCDiamond, edge: tuple[int, int]) -> list[dict]:
    """All raw functions [q] -> [q] obtained by varying edge source only."""
    src, tgt = map(int, edge)
    preds = tuple(int(x) for x in ds.joint.preds[tgt])
    positions = [i for i, p in enumerate(preds) if p == src]
    if len(positions) != 1:
        # The present diamond has one explicit interface edge; return a marked row
        # rather than silently doing the wrong thing on unusual multigraphs.
        return [dict(edge=(src, tgt), target=tgt, source=src, status=f"source_position_count_{len(positions)}")]
    pos = positions[0]
    other_positions = [i for i in range(len(preds)) if i != pos]
    out: list[dict] = []
    rule = ds.joint.rules[tgt]
    for ctx in _context_rows(len(other_positions), ds.q):
        vals = [0] * len(preds)
        for j, ppos in enumerate(other_positions):
            vals[ppos] = int(ctx[j])
        f = []
        for a in range(ds.q):
            vals[pos] = int(a)
            f.append(int(rule[_rule_index(vals, ds.q)]))
        ft = tuple(f)
        rank = len(set(ft))
        is_perm = sorted(ft) == list(range(ds.q))
        out.append(dict(
            edge=(src, tgt),
            source=src,
            target=tgt,
            status="ok",
            context=tuple(int(x) for x in ctx),
            function=ft,
            rank=int(rank),
            is_permutation=bool(is_perm),
            permutation=ft if is_perm else None,
        ))
    return out


def interface_edges(ds: S.SCDCDiamond, family: EdgeFamily = "all") -> dict[str, tuple[tuple[int, int], ...]]:
    fam = {
        "BC": tuple(ds.interface_BC),
        "BD": tuple(ds.interface_BD),
        "CA": tuple(ds.interface_CA),
        "DA": tuple(ds.interface_DA),
    }
    if family == "all":
        return fam
    return {family: fam[family]}


def audit_edge_family(ds: S.SCDCDiamond, family: EdgeFamily = "all") -> dict:
    fam = interface_edges(ds, family)
    rows = []
    edge_summaries = {}
    all_perms = []
    for name, edges in fam.items():
        edge_perms = []
        edge_total = 0
        edge_perm_contexts = 0
        edge_rank_hist = Counter()
        edge_fn_hist = Counter()
        for edge in edges:
            frows = restricted_functions_for_edge(ds, edge)
            for r in frows:
                if r.get("status") != "ok":
                    continue
                rows.append((name, edge, r))
                edge_total += 1
                edge_rank_hist[int(r["rank"])] += 1
                edge_fn_hist[tuple(r["function"])] += 1
                if r["is_permutation"]:
                    p = tuple(int(x) for x in r["permutation"])
                    edge_perm_contexts += 1
                    edge_perms.append(p)
                    all_perms.append(p)
        g = generated_group(edge_perms, ds.q)
        edge_summaries[name] = dict(
            n_edges=int(len(edges)),
            contexts_total=int(edge_total),
            permutation_contexts=int(edge_perm_contexts),
            permutation_fraction=float(edge_perm_contexts / edge_total) if edge_total else math.nan,
            unique_permutations=int(len(set(edge_perms))),
            rank_hist={int(k): int(v) for k, v in sorted(edge_rank_hist.items())},
            top_functions=[dict(function=list(map(int, k)), count=int(v), is_permutation=(sorted(k) == list(range(ds.q)))) for k, v in edge_fn_hist.most_common(8)],
            generators=[perm_to_cycles(p) for p in sorted(set(edge_perms))],
            **classify_group(g, ds.q),
        )
    group = generated_group(all_perms, ds.q)
    agg = classify_group(group, ds.q)
    agg.update(
        contexts_total=int(sum(v["contexts_total"] for v in edge_summaries.values())),
        permutation_contexts=int(sum(v["permutation_contexts"] for v in edge_summaries.values())),
        permutation_fraction=float(
            sum(v["permutation_contexts"] for v in edge_summaries.values()) /
            max(1, sum(v["contexts_total"] for v in edge_summaries.values()))
        ),
        unique_permutation_generators=int(len(set(all_perms))),
        generator_cycles=json.dumps([perm_to_cycles(p) for p in sorted(set(all_perms))]),
        edge_summaries=json.dumps(edge_summaries),
    )
    return agg


