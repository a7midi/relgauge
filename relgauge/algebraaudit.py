def load_winner_bundle(path: str) -> list[dict]:
    return BS.load_winner_records(path)


def run_algebra_audit(
    winner_path: str,
    *,
    top_n: int = 20,
    edge_family: EdgeFamily = "all",
    verbose: bool = True,
) -> pd.DataFrame:
    records = load_winner_bundle(winner_path)
    if top_n > 0:
        records = records[: int(top_n)]
    rows = []
    for i, rec in enumerate(records):
        ds = rec.get("diamond")
        if ds is None:
            raise ValueError(f"winner record {i} has no diamond object")
        metrics = rec.get("metrics", {}) or {}
        row = dict(
            candidate_index=int(i),
            q=int(ds.q),
            w=int(ds.w),
            kB=int(ds.kB),
            kC=int(ds.kC),
            kD=int(ds.kD),
            kA=int(ds.kA),
            saved_run=metrics.get("run", None),
            saved_generation=metrics.get("generation", None),
            saved_candidate=metrics.get("candidate", None),
            saved_exact_residual_qcoords=metrics.get("exact_residual_qcoords", None),
            saved_blind_fitness=metrics.get("blind_fitness", None),
        )
        row.update(audit_edge_family(ds, edge_family))
        rows.append(row)
        if verbose:
            print(
                f"algebra winner={i} group={row['group_name']} order={row['group_order']} "
                f"zq_like={row['group_zq_like']} perm_frac={row['permutation_fraction']:.3f}",
                flush=True,
            )
    return pd.DataFrame(rows)


def analyze_algebra_audit(df: pd.DataFrame) -> dict:
    if df.empty:
        return {"verdict": "NO DATA"}
    name_counts = df["group_name"].value_counts().to_dict()
    zq_frac = float(df["group_zq_like"].mean()) if "group_zq_like" in df else 0.0
    sym_frac = float(df["group_symmetric"].mean()) if "group_symmetric" in df else 0.0
    trans_frac = float(df["group_transitive"].mean()) if "group_transitive" in df else 0.0
    abel_frac = float(df["group_abelian"].mean()) if "group_abelian" in df else 0.0
    mean_order = float(df["group_order"].mean())
    mean_perm_frac = float(df["permutation_fraction"].mean())
    q_vals = sorted(set(int(x) for x in df["q"].dropna().tolist()))

    if zq_frac >= 0.75:
        verdict = "Z_q-LIKE ALGEBRA SIGNAL: selected interfaces generate transitive cyclic phase groups"
    elif sym_frac >= 0.75:
        verdict = "FULL PERMUTATION SIGNAL: selected interfaces generate S_q-like label symmetry, not U(1)-like phase"
    elif trans_frac >= 0.75 and abel_frac < 0.5:
        verdict = "NONABELIAN TRANSITIVE LABEL SYMMETRY: gauge-like, but not electromagnetic U(1)-like"
    elif mean_perm_frac < 0.25:
        verdict = "WEAK LOCAL-PERMUTIVE STRUCTURE: exact S exists but local raw rules rarely act as permutations"
    elif abel_frac >= 0.75:
        verdict = "ABELIAN FINITE LABEL SYMMETRY: possible discrete charge sector; inspect group names"
    else:
        verdict = "MIXED FINITE LABEL ALGEBRAS: inspect edge-level summaries before holonomy design"

    top = df.sort_values(["group_zq_like", "group_order", "permutation_fraction"], ascending=False).head(10)
    return dict(
        verdict=verdict,
        n_winners=int(len(df)),
        q_values=q_vals,
        group_name_counts={str(k): int(v) for k, v in name_counts.items()},
        zq_like_fraction=float(zq_frac),
        symmetric_fraction=float(sym_frac),
        transitive_fraction=float(trans_frac),
        abelian_fraction=float(abel_frac),
        mean_group_order=float(mean_order),
        mean_permutation_fraction=float(mean_perm_frac),
        top_winners=top[[
            "candidate_index", "group_name", "group_order", "group_zq_like",
            "group_abelian", "group_cyclic", "group_transitive",
            "permutation_fraction", "unique_permutation_generators",
            "group_element_order_hist",
        ]].to_dict(orient="records"),
    )


def plot_algebra_audit(df: pd.DataFrame, path: str) -> None:
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    if df.empty:
        raise ValueError("empty dataframe")
    fig, ax = plt.subplots(1, 3, figsize=(15, 4))
    counts = df["group_name"].value_counts()
    ax[0].bar(range(len(counts)), counts.values)
    ax[0].set_xticks(range(len(counts)))
    ax[0].set_xticklabels(counts.index.tolist(), rotation=30, ha="right")
    ax[0].set_ylabel("winner count")
    ax[0].set_title("generated group type")

    ax[1].scatter(df["permutation_fraction"], df["group_order"], alpha=0.75)
    ax[1].set_xlabel("permutation-context fraction")
    ax[1].set_ylabel("generated group order")
    ax[1].set_title("local permutivity vs algebra size")

    # Element-order histograms as stacked-ish lines by winner.
    for _, row in df.head(12).iterrows():
        hist = json.loads(row["group_element_order_hist"])
        xs = sorted(int(k) for k in hist.keys())
        ys = [hist[str(x)] if str(x) in hist else hist.get(x, 0) for x in xs]
        ax[2].plot(xs, ys, "o-", alpha=0.45)
    ax[2].set_xlabel("element order")
    ax[2].set_ylabel("count in group")
    ax[2].set_title("element-order fingerprints")

    fig.tight_layout()
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    fig.savefig(path, dpi=130)
    plt.close(fig)


def _main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(description="Extract finite algebra generated by saved blind-selection winners.")
    p.add_argument("winner_path")
    p.add_argument("--top-n", type=int, default=20)
    p.add_argument("--edge-family", choices=["BC", "BD", "CA", "DA", "all"], default="all")
    p.add_argument("--out", default=None)
    p.add_argument("--plot", default=None)
    p.add_argument("--quiet", action="store_true")
    args = p.parse_args(argv)

    df = run_algebra_audit(args.winner_path, top_n=args.top_n, edge_family=args.edge_family, verbose=not args.quiet)
    summary = analyze_algebra_audit(df)
    print(json.dumps(summary, indent=2, default=float))

    if args.out:
        os.makedirs(os.path.dirname(args.out) or ".", exist_ok=True)
        df.to_csv(args.out, index=False)
        root, _ = os.path.splitext(args.out)
        with open(root + "_summary.json", "w", encoding="utf-8") as f:
            json.dump(summary, f, indent=2, default=float)
        if not args.quiet:
            print(f"wrote {args.out}")
            print(f"wrote {root + '_summary.json'}")
    if args.plot:
        plot_algebra_audit(df, args.plot)
        if not args.quiet:
            print(f"wrote {args.plot}")
    return 0


if __name__ == "__main__":
    raise SystemExit(_main())
